    private void cargarTabla() {

        DefaultTableModel modelo = (DefaultTableModel) tbltabla.getModel();
        modelo.setRowCount(0);

        for (Tarea t : tareaController.obtenerTareas()) {
            Object[] fila = new Object[]{
                t.getId(),
                t.getNombre(),
                t.getFecha(),
                t.getDescripcion(),
                t.getEstado()
            };
            modelo.addRow(fila);
        }
}

