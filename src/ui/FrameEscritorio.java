    private void btnañadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnañadirActionPerformed
       try {
            int id = Integer.parseInt(txtid.getText());
            String nombre = txtnombre.getText();
            String fecha = txtfecha.getText();
            String descripcion = txtdescripcion.getText();
            String estado = txtestado.getText();

            if(nombre.isEmpty() || fecha.isEmpty() || descripcion.isEmpty() || estado.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor llene todos los campos.");
                return;
            }

            Tarea tarea = new Tarea(id, nombre, fecha, descripcion, estado);
            boolean success = tareaController.añadirTarea(tarea);

            if(success) {
                JOptionPane.showMessageDialog(this, "Tarea añadida correctamente.");
                cargarTabla();

                txtid.setText("");
                txtnombre.setText("");
                txtfecha.setText("");
                txtdescripcion.setText("");
                txtestado.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Error al añadir la tarea. ¿ID duplicado?");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un número entero.");
        }
    

    }//GEN-LAST:event_btnañadirActionPerformed

